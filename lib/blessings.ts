export function getRandomBlessing(): Blessing {
  const randomIndex = Math.floor(Math.random() * blessings.length);
  return blessings[randomIndex];
}

export function getTodaysBlessing(): Blessing {
  // Use the day of year to ensure same blessing all day
  const now = new Date();
  const start = new Date(now.getFullYear(), 0, 0);
  const diff = now.getTime() - start.getTime();
  const oneDay = 1000 * 60 * 60 * 24;
  const dayOfYear = Math.floor(diff / oneDay);
  
  // Use modulo to cycle through blessings based on day of year
  const index = dayOfYear % blessings.length;
  return blessings[index];
}
